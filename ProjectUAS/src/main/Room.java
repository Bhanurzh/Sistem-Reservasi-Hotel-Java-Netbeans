    private void ShowRooms(){
        int column;
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/hotels", "root", "");
            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM room ");
            
            ResultSetMetaData rsd = rs.getMetaData();
            column = rsd.getColumnCount();
            
            dtm = (DefaultTableModel)RoomTable.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                
                for(int i=1; i<=column; i++){
                    v2.add(rs.getString("nomor_kamar"));
                    v2.add(rs.getString("nama_kamar"));
                    v2.add(rs.getString("tipe_kamar"));
                    v2.add(rs.getString("status_kamar"));
                    v2.add(rs.getString("harga"));
                }
                dtm.addRow(v2);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Room.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
