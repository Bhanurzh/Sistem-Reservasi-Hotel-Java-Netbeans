    private void ButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEditActionPerformed
        // TODO add your handling code here:
        if(tfNama.getText().isEmpty() || cbKategori.getSelectedIndex() == -1 || cbStatus.getSelectedIndex() == -1 || tfHarga.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Data Tidak Boleh Kosong!");
        }else{
            try {
                CountRooms();
                con = DriverManager.getConnection("jdbc:mysql://localhost/hotels", "root", "");
                PreparedStatement pres = con.prepareStatement("UPDATE room set nama_kamar=?, tipe_kamar=?, status_kamar=?, harga=? where nomor_kamar=?");
                pres.setInt(5, key);
                pres.setString(1, tfNama.getText().toString());
                pres.setString(2, cbKategori.getSelectedItem().toString());
                pres.setString(3, cbStatus.getSelectedItem().toString());
                pres.setInt(4, Integer.valueOf(tfHarga.getText().toString()));
                int row = pres.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Kamar telah Diperbarui!");
                con.close();
                ShowRooms();
                
            } catch (SQLException ex) {
                Logger.getLogger(Room.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_ButtonEditActionPerformed

